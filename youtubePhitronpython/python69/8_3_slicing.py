# list slicing format list[start:end:step]

a = [12, 13, 4, "Phitron", "Mango", [12.5, 19]]
print(a[::]) #full list print # list[start:end:step]
print(a[1::2])#list[start:end:step]
print(a[2::4])
print(a)
print(a[::-1]) # New technique list reverse print
print(a[-1::-1])
print(a[-1:-3:-1]) # list[start:end:step]
